package com.QueueDemo;

public class Trainee {
	
	int rank;
	String name;
	public Trainee(int rank, String name) {
		super();
		this.rank = rank;
		this.name = name;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
